//FOR LOOP
for (let i = 0 /*Initialisation Of Variable*/ ; i < 10 /*Condition Check*/; i++ /*Value Change*/) {
    console.log("HELLO !") /*Code Execution*/
}
//Steps in which For Loop Executes
/*
1.Initialise A Variable
2.Check The Condition
3.Execute The Code
4.Change The Value
*/


//WHILE LOOP
let i=20
while (i>10) {
    console.log(i)
    i--
}
//Steps in which While Loop Executes
/*
1.Initialise A Variable
2.Check The Condition
3.Execute The Code
4.Change The Value
*/


//DO WHILE LOOP
let j=20
do {
    console.log(j)
} while (j<10); //Even When Condition False Code Executed Once
//Steps in which Do While Loop Executes
/*
1.Initialise A Variable
2.Check The Condition
3.Execute The Code
4.Change The Value
*/